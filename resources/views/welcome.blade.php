    <script>
    "use strict";

    (function() {
        // --- Función de Utilidad para Verificar Elementos ---
        const getElement = (selector, context = document, warn = false) => {
            const element = context.querySelector(selector);
            if (!element && warn) {
                console.warn(`Element not found for selector: ${selector}`);
            }
            return element;
        };

        document.addEventListener('DOMContentLoaded', function() {
            const contactForm = getElement('#contactForm');
            if (!contactForm) return;

            const formMessage = getElement('#formMessage', contactForm);
            const submitButton = getElement('button[type="submit"]', contactForm);

            if (!formMessage || !submitButton) {
                console.error("Required elements #formMessage or submit button missing in #contactForm.");
                return;
            }

            const originalButtonHtml = submitButton.innerHTML;
            const SPINNER_HTML = '<span class="loading-spinner" aria-hidden="true"></span> Enviando...';
            const formAction = '{{ route("contact.submit") }}'; // ¡Aquí Blade procesará la ruta!

            contactForm.addEventListener('submit', function(event) {
                event.preventDefault();

                // --- Resetear ---
                submitButton.disabled = true;
                submitButton.innerHTML = SPINNER_HTML;
                formMessage.style.display = 'none';
                formMessage.className = 'form-message';
                formMessage.textContent = '';
                formMessage.removeAttribute('role');

                const formData = new FormData(contactForm);

                // --- Fetch ---
                fetch(formAction, {
                    method: 'POST',
                    body: formData,
                    headers: { 'Accept': 'application/json' }
                })
                .then(response => {
                    if (!response.ok) {
                        return response.text().then(text => {
                            throw new Error(`Server Error: ${response.status} ${response.statusText}. ${text.substring(0,100)}`);
                        });
                    }
                    return response.json();
                })
                .then(data => {
                    if (data && data.status === 'success') {
                        formMessage.textContent = data.message || '¡Mensaje enviado!';
                        formMessage.classList.add('success-message');
                        formMessage.setAttribute('role', 'status');
                        contactForm.reset();
                        console.info("Form submitted successfully:", data);
                    } else {
                        throw new Error(data?.message || 'Respuesta inválida del servidor.');
                    }
                })
                .catch(error => {
                    console.error('Error submitting contact form:', error);
                    formMessage.textContent = (error.message.includes('NetworkError') || error.message.includes('fetch'))
                        ? 'Error de conexión. Revisa tu red.'
                        : `Error: ${error.message}`;
                    formMessage.classList.add('error-message');
                    formMessage.setAttribute('role', 'alert');
                })
                .finally(() => {
                    submitButton.disabled = false;
                    submitButton.innerHTML = originalButtonHtml;
                    formMessage.style.display = 'block';
                });
            });

            console.log("Contact Form Handler Initialized.");
        }); // Fin DOMContentLoaded
    })(); // Fin IIFE
    </script>
